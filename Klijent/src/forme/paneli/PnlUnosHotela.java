    private void popuniGradove() {
        try {
            List<Grad> gradovi = Kontroler.getInstanca().vratiGradove();
            cmbGrad.setModel(new DefaultComboBoxModel(gradovi.toArray()));
        } catch (Exception ex) {
            Logger.getLogger(PnlUnosHotela.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void isprazniPoljaForme() {
        txtNaziv.setText("");
        txtAdresa.setText("");
        txtEmail.setText("");
        txtTelefon.setText("");
        cmbGrad.setSelectedIndex(0);
    }

    private boolean validacija() {
        String naziv = txtNaziv.getText().trim();
        String email = txtEmail.getText().trim();
        String adresa = txtAdresa.getText().trim();
        String telefon = txtTelefon.getText().trim();

        if (naziv.isEmpty() || email.isEmpty() || adresa.isEmpty() || telefon.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja su obavezna!", "Greska", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "E-mail mora da sadrzi '@'", "Greska", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }


    private void btnUnesiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnesiActionPerformed
        String naziv = txtNaziv.getText().trim();
        Grad grad = (Grad) cmbGrad.getSelectedItem();
        String email = txtEmail.getText().trim();
        String adresa = txtAdresa.getText().trim();
        String telefon = txtTelefon.getText().trim();
        if (validacija()) {
            try {
                Hotel hotel = new Hotel(0, naziv, adresa, telefon, email, grad);
                Kontroler.getInstanca().unesiHotel(hotel);
                JOptionPane.showMessageDialog(this, "Uspesno ste uneli hotel!");
                isprazniPoljaForme();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnUnesiActionPerformed



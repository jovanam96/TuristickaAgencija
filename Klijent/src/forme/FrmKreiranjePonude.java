    private void isprazniPoljaForme() {
        cmbDrzava.setSelectedIndex(0);
        dpcDatumOd.setSelectedDate(Calendar.getInstance());
        dpcDatumDo.setSelectedDate(Calendar.getInstance());
        tblStavke.setModel(new ModelTabeleStavkePonude(new ArrayList<>()));
    }

    private boolean validacija() {
        Drzava d = (Drzava) cmbDrzava.getSelectedItem();
        Grad g = (Grad) cmbGrad.getSelectedItem();
        Hotel h = (Hotel) cmbHotel.getSelectedItem();
        Date datumOd = dpcDatumOd.getSelectedDate().getTime();
        Date datumDo = dpcDatumOd.getSelectedDate().getTime();
        ModelTabeleStavkePonude model = (ModelTabeleStavkePonude) tblStavke.getModel();
        List<StavkaPonude> stavke = model.vratiStavke();

        if (d == null || g == null || h == null) {
            JOptionPane.showMessageDialog(this, "Sva polja su obavezna!", "Greska", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (stavke.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ponuda mora sadrzati barem jednu stavku!", "Greska", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;

    }

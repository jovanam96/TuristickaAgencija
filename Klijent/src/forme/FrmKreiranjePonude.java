    private void btnSacuvaj1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvaj1ActionPerformed
        Drzava d = (Drzava) cmbDrzava.getSelectedItem();
        Grad g = (Grad) cmbGrad.getSelectedItem();
        Hotel h = (Hotel) cmbHotel.getSelectedItem();
        Date datumOd = dpcDatumOd.getSelectedDate().getTime();
        Date datumDo = dpcDatumOd.getSelectedDate().getTime();
        ModelTabeleStavkePonude model = (ModelTabeleStavkePonude) tblStavke.getModel();
        List<StavkaPonude> stavke = model.vratiStavke();
        Ponuda p = new Ponuda(0, datumOd, datumDo, h, g, stavke);
        try {
            if (validacija()) {
                Kontroler.getInstanca().kreirajPonudu(p);
                JOptionPane.showMessageDialog(this, "Uspesno sacuvana ponuda!");
                isprazniPoljaForme();
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmKreiranjePonude.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSacuvaj1ActionPerformed



    private void srediFormu() {
        pnlKlijent = new PnlKlijent(FrmMode.PRIKAZ);
        pnlKlijenta.add(pnlKlijent);
        pnlKlijent.setEnabled(false);
        jPanel1.setEnabled(false);
        
        switch(mode) {
            case UNOS:
                lblBrojrezervacije.setVisible(false);
                txtBrojRezervacije.setVisible(false);
                break;
            case PRIKAZ:
                btnIzaberiKlijenta.setVisible(false);
                btnIzaberiPonudu.setVisible(false);
                btnKreirajRezervaciju.setVisible(false);
                
                Rezervacija r = (Rezervacija) Sesija.getInstanca().getMapa().get("Rezervacija");
                txtBrojRezervacije.setText(r.getBrojRezervacije() + "");
                ubaciKlijenta(r.getKlijent());
                ubaciPonudu(r.getStavkaPonude());
                break;
        }
        
    }
    
    public void ubaciKlijenta(Klijent k) {
        this.k = k;
        pnlKlijent.setTxtIme(k.getIme());
        pnlKlijent.setTxtJMBG(k.getJmbg());
        pnlKlijent.setTxtPrezime(k.getPrezime());
        pnlKlijent.setTxtBrojPasosa(k.getBrojPasosa());
        pnlKlijent.setTxtTelefon(k.getTelefon());
        pnlKlijent.setTxtEmail(k.getEmail());
    }
    
    public void ubaciPonudu(StavkaPonude sp) {
        this.sp = sp;
        txtDrzava.setText(sp.getPonuda().getGrad().getDrzava().getNaziv());
        txtGrad.setText(sp.getPonuda().getGrad().toString());
        SimpleDateFormat sf = new SimpleDateFormat("dd.MM.yyyy");
        txtDatumOd.setText(sf.format(sp.getPonuda().getDatumOd()));
        txtDatumDo.setText(sf.format(sp.getPonuda().getDatumDo()));
        txtHotel.setText(sp.getPonuda().getHotel().toString());
        txtTipSobe.setText(sp.getTipSobe().toString());
        txtCena.setText(sp.getCena() + "");
    }
    
    private void isprazniPoljaForme() {
        pnlKlijent.isprazniPoljaForma();
        txtCena.setText("");
        txtDatumDo.setText("");
        txtDatumOd.setText("");
        txtDrzava.setText("");
        txtGrad.setText("");
        txtHotel.setText("");
        txtTipSobe.setText("");
    }
    

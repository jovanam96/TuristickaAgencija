    private void btnKreirajRezervacijuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajRezervacijuActionPerformed
        try {
            if(k==null || sp==null) {
                JOptionPane.showMessageDialog(this, "Izbarite klijenta i ponudu", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Rezervacija r = new Rezervacija(0, k, sp, new Date());
            if (sp.getKapacitet() > 0) {
                sp.setKapacitet(sp.getKapacitet() - 1);
                Kontroler.getInstanca().kreirajRezervaciju(r);
                JOptionPane.showMessageDialog(this, "Uspesno kreirana rezervacija");
                isprazniPoljaForme();
            } else {
                JOptionPane.showMessageDialog(this, "Popunjen kapacitet za ovu ponudu", "Greska", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnKreirajRezervacijuActionPerformed



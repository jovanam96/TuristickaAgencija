    private void txtImeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtImeKeyReleased
        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        Klijent k = new Klijent();
        if (!ime.isEmpty()) {
            k.setIme(ime);
        }
        if (!prezime.isEmpty()) {
            k.setPrezime(prezime);
        }
        try {
            List<Klijent> list = Kontroler.getInstanca().pretraziKlijente(k);
            tblKlijenti.setModel(new ModelTabeleKlijenti(list));
        } catch (Exception ex) {
            Logger.getLogger(FrmPretragaKlijenata.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtImeKeyReleased

    private void txtPrezimeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrezimeKeyReleased
        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        Klijent k = new Klijent();
        if (!ime.isEmpty()) {
            k.setIme(ime);
        }
        if (!prezime.isEmpty()) {
            k.setPrezime(prezime);
        }
        List<Klijent> list;
        try {
            list = Kontroler.getInstanca().pretraziKlijente(k);
            if (list.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ne postoje rezultati pretrage");
                return;
            }
            tblKlijenti.setModel(new ModelTabeleKlijenti(list));
        } catch (Exception ex) {
            Logger.getLogger(FrmPretragaKlijenata.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtPrezimeKeyReleased


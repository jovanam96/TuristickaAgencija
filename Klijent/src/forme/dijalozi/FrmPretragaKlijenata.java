    private void srediFormu() {
        switch (mode) {
            case PRETRAGA:
                btnIzmeniKlijenta.setVisible(false);
                break;
            case IZMENA:
                btnIzaberiKlijenta.setVisible(false);
                btnUnesiNovogKlijenta.setVisible(false);
                break;
        }
    }

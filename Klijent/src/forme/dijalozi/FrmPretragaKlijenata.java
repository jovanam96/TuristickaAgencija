    private void btnIzaberiKlijentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiKlijentaActionPerformed
        int red = tblKlijenti.getSelectedRow();
        if (red != -1) {
            ModelTabeleKlijenti model = (ModelTabeleKlijenti) tblKlijenti.getModel();
            Klijent k = model.vratiKlijenta(red);
            FrmKreiranjeRezervacije frm = (FrmKreiranjeRezervacije) getParent();
            frm.ubaciKlijenta(k);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Selektujte red", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzaberiKlijentaActionPerformed

    private void btnIzmeniKlijentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniKlijentaActionPerformed
        int red = tblKlijenti.getSelectedRow();
        if (red != -1) {
            ModelTabeleKlijenti model = (ModelTabeleKlijenti) tblKlijenti.getModel();
            Klijent k = model.vratiKlijenta(red);
            Sesija.getInstanca().getMapa().clear();
            Sesija.getInstanca().getMapa().put("Klijent", k);
            JDialog dialog = new JDialog(this, true);
            PnlKlijent pnl = new PnlKlijent(FrmMode.IZMENA);
            dialog.setContentPane(pnl);
            dialog.pack();
            dialog.setLocationRelativeTo(null);
            dialog.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Selektujte red", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniKlijentaActionPerformed


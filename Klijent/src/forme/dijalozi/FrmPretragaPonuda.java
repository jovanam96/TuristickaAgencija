    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        Date datumOd = dpcDatumOd.getSelectedDate().getTime();
        Date datumDo = dpcDatumDo.getSelectedDate().getTime();
        Grad g = (Grad) cmbGrad.getSelectedItem();
        Ponuda p = new Ponuda();
        p.setDatumOd(datumOd);
        p.setDatumDo(datumDo);
        if (!g.getNaziv().toLowerCase().equals("svi gradovi")) {
            p.setGrad(g);
        }

        try {
            List<Ponuda> ponude = Kontroler.getInstanca().pretraziPonude(p);
            if(ponude.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ne postoje rezultati pretrage");
                tblStavkePonude.setModel(new ModelTabeleStavkePonude(new ArrayList<>()));
            }
            tblPonude.setModel(new ModelTabelePonude(ponude));
            pnlStavkePonude.setVisible(false);
        } catch (Exception ex) {
            Logger.getLogger(FrmPretragaPonuda.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnPrikaziStavkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziStavkeActionPerformed
        int red = tblPonude.getSelectedRow();
        if (red != -1) {
            ModelTabelePonude model = (ModelTabelePonude) tblPonude.getModel();
            Ponuda p = model.vratiPonudu(red);
            tblStavkePonude.setModel(new ModelTabeleStavkePonude(p.getStavke()));
            pnlStavkePonude.setVisible(true);
            if (mode == FrmMode.PRIKAZ) {
                btnIzaberiPonudu.setVisible(true);
            }
            pack();
        } else {
            JOptionPane.showMessageDialog(this, "Selektujte red", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPrikaziStavkeActionPerformed

    private void btnIzaberiPonuduActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiPonuduActionPerformed
        int red = tblStavkePonude.getSelectedRow();
        if (red != -1) {
            ModelTabeleStavkePonude model = (ModelTabeleStavkePonude) tblStavkePonude.getModel();
            StavkaPonude sp = model.vratiStavkuPonude(red);
            FrmKreiranjeRezervacije frm = (FrmKreiranjeRezervacije) getParent();
            frm.ubaciPonudu(sp);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Selektujte red", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzaberiPonuduActionPerformed


    private void popuniGradove() {
        try {
            Grad g = new Grad(0, "Svi gradovi", null);
            List<Grad> list = new ArrayList<>();
            list.add(g);
            list.addAll(Kontroler.getInstanca().vratiGradove());
            cmbGrad.setModel(new DefaultComboBoxModel(list.toArray()));
        } catch (Exception ex) {
            Logger.getLogger(FrmPretragaPonuda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniTabelu() {
        tblPonude.setModel(new ModelTabelePonude(new ArrayList<>()));
    }

